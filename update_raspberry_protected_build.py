def obfuscate_css(file_path):
    """–û–±—Ñ—É—Å–∫–∞—Ü–∏—è CSS —Ñ–∞–π–ª–∞"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # –£–¥–∞–ª—è–µ–º –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏
    content = re.sub(r'/\*.*?\*/', '', content, flags=re.DOTALL)
    
    # –ú–∏–Ω–∏—Ñ–∏–∫–∞—Ü–∏—è
    content = re.sub(r'\s+', ' ', content)
    content = re.sub(r';\s*', ';', content)
